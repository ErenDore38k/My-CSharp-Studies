//Normal döngüler zaten maliyetlidir, iç içe döngüler daha da maliyetlidir.

for (int i = 0; i < 3; i++)
{
    for (int j = 0; j < 3; j++)
    {
        Console.WriteLine("Hilmi");
    }
}

